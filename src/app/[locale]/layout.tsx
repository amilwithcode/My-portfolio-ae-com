
import React from "react";
import type { Metadata } from "next";
import { NextIntlClientProvider } from "next-intl";
import { getMessages } from "next-intl/server";
import { ThemeProvider } from "next-themes";
import "@/style/globals.css";
import AuthContextProvider from "@/context/AuthContext";
import Navbar from "@/components/Navbar";
import FooterSec from "@/components/FooterSec";
import ChangeTheme from "@/ui/ChangeTheme";
import LocaleSwitcher from "@/components/LocaleSwitcher";
import { notFound } from "next/navigation";
// import {setRequestLocale} from 'next-intl/server'
import { routing } from "@/i18n/routing";

export const metadata: Metadata = {
    title: "Amil's portfolio",
    description: "This website generated by create next app",
    icons: [
        { rel: "icon", url: "/favicon/favicon.ico" },
        {
            rel: "apple-touch-icon",
            sizes: "180x180",
            url: "/favicon/favicon.ico",
        },
    ],
};


export default async function LocaleLayout({
    children,
    params: { locale }
}: {
    children: React.ReactNode;
    params: { locale: string };
}) {
    // Ensure that the incoming `locale` is valid
    if (!routing.locales.includes(locale as any)) {
        notFound();
    }
    // setRequestLocale(locale)
    // const locale = await getLocale();
    const messages = await getMessages();


    return (
        <html lang={locale} suppressHydrationWarning>
            <body>
                <AuthContextProvider>
                    <ThemeProvider attribute="class">
                        <NextIntlClientProvider

                            messages={messages}
                        >
                            <header>
                                <div className=" w-full  text-black dark:bg-black px-10 dark:text-white ">
                                    <ChangeTheme />{" "}

                                    <LocaleSwitcher  />
                                    <Navbar />
                                </div>
                            </header>
                            <main>
                                {children}
                            </main>

                            <footer className=" flex  flex-wrap text-black dark:bg-black dark:text-white items-center justify-center">
                                <FooterSec />
                            </footer>
                        </NextIntlClientProvider>
                    </ThemeProvider>
                </AuthContextProvider>
            </body>
        </html>
    );
}
